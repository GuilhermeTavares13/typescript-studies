// Only use lowercase, like number, string, boolean, true, false... 

// Number type, can be int, float, decimal whatever
const number: number = 89;

// String type, can be text
const nameOfThePerson: string = 'Carlos';

// Boolean, hold true or false
const hasClicked: boolean = false;

// Arrays
// Can be both declared like this:
const numbers: number[] = [1,2,3,4];

// Or like this:
const numbers2: Array<number> = [1,2,3,4];